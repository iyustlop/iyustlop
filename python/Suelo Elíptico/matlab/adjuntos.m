function[Adj]=adjuntos(A)
    Adj=ones(3);
    Adj(1,1)=A(2,2)*A(3,3)-A(2,3)*A(3,2);
    Adj(1,2)=A(1,3)*A(2,2)-A(1,2)*A(2,3);
    Adj(1,3)=A(1,2)*A(2,3)-A(1,3)*A(2,2);
    Adj(2,2)=A(1,1)*A(3,3)-A(1,3)*A(3,1);
    Adj(2,3)=A(1,2)*A(1,3)-A(1,1)*A(2,3);
    Adj(3,3)=A(1,1)*A(2,2)-A(1,2)*A(2,1);
    Adj(2,1)=Adj(1,2);
    Adj(3,1)=Adj(1,3);
    Adj(3,2)=Adj(2,3);
      
end